// These are only some of the examples
// Full list of operators can be found at https://learn.microsoft.com/en-us/dotnet/csharp/language-reference/operators/

// Expression using additive binary operator
int sum = 10 + 10;
Console.WriteLine(sum);

// Expression using additive binary operator with variables
int firstNumber = 10;
int secondNumber = 10;
int variableSum = firstNumber + secondNumber;
Console.WriteLine(variableSum);

// Expression using logical not unary opeartor
bool isEnabled = !false;
Console.WriteLine(isEnabled);

// Mixed expression using additive and multiplicative operators
var result = 10 + 10 * 2;
Console.WriteLine(result);